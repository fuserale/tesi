A = table2array(readtable('../../../../3cl_S01R01.csv'));
AccM = A(:,2:4);
div = 0.00980665;
AccM = AccM * div;
samplePeriod = 1/64;
filtCutOff = 10;
[b, a] = butter(2, (2*filtCutOff)/(1/samplePeriod), 'high');
AccMFilt = filtfilt(b, a, AccM);
%AccMFilt = abs(AccMFilt);
time = (0:length(AccMFilt)-1)'*samplePeriod;
vel=cumtrapz(time,AccMFilt);
pos=cumtrapz(time,vel);
figure;
plot(time,AccMFilt);
ylabel('Acceleration [m/s^2]');
figure;
plot(time,vel);
ylabel('Velocity [m/s]');
figure;
plot(time,pos);
ylabel('Position [m]');
xlabel('Time [s]');